// --- ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ -------------------------------------------------

function getProfile(counts) {
  const max = Math.max(counts.A, counts.B, counts.V);
  // При ничьей приоритет: A > B > V
  if (counts.A === max) return 'A';
  if (counts.B === max) return 'B';
  return 'V';
}

async function checkSubscription(ctx) {
  try {
    const member = await ctx.telegram.getChatMember(CHANNEL_ID, ctx.from.id);
    return ['member', 'administrator', 'creator'].includes(member.status);
  } catch (e) {
    return false;
  }
}

function sendQuestion(ctx, questionIndex) {
  const q = questions[questionIndex];
  const buttons = q.options.map((opt, i) =>
    [Markup.button.callback(opt.text, `answer_${questionIndex}_${i}`)]
  );
  return ctx.reply(q.text, Markup.inlineKeyboard(buttons));
}

